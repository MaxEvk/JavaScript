//não se pode modificar o valor de uma constante
//Não utilize var, utilize const
//Se quiser modificar o valor, utilize LET
const nome= 'João';
console.log(nome);

let nome2;
nome2 = 'Max';
console.log(nome2);

//manipulando constantes
